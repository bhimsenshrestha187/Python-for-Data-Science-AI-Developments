#We can access elements in a set using in membership operator 
# as sets are unindexe3d and donot support accessing by indexing.

set1 = set(["Geeks", "For", "Geeks.",1,6,"a"])

# Accessing element using For loop
for i in set1:
    print(i)

# Checking the element# using in keyword
print("Geeks" in set1)


